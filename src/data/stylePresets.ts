export function getPreset(id: string | null): StylePreset | undefined {
  return STYLE_PRESETS.find((p) => p.id === id)
}

/**
 * サムネ背景。/thumbs/<id>.webp を最前面、プリセットのグラデーションを背面に重ねる。
 * 画像が未生成（404）の場合は自動でグラデーションにフォールバックする。
 */
export function thumbBg(preset: StylePreset): string {
  return `url("/thumbs/${preset.id}.webp") center / cover no-repeat, ${preset.thumbnailClass}`
}

/** プリセット群から印象タグの一覧を抽出（ライブラリの絞り込み用） */
export function allImpressions(): string[] {
  return Array.from(new Set(STYLE_PRESETS.flatMap((p) => p.impressions)))
}
/** プリセット群から色味タグの一覧を抽出 */
export function allColorTones(): string[] {
  return Array.from(new Set(STYLE_PRESETS.flatMap((p) => p.colorTones)))
}
